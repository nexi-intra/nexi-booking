CREATE OR REPLACE PROCEDURE proc.create_booking(
    p_actor_name VARCHAR,
    p_params JSONB,
    OUT p_id INTEGER
)
LANGUAGE plpgsql
AS $BODY$
DECLARE
    v_tenant VARCHAR COLLATE pg_catalog."default" ;
    v_searchindex VARCHAR COLLATE pg_catalog."default" ;
    v_name VARCHAR COLLATE pg_catalog."default" ;
    v_description VARCHAR COLLATE pg_catalog."default";
    v_desk_id INTEGER;
    v_user_id INTEGER;
    v_fromdatetime TIMESTAMP WITH TIME ZONE;
    v_todatetime TIMESTAMP WITH TIME ZONE;
        v_audit_id integer;  -- Variable to hold the OUT parameter value
    p_auditlog_params jsonb;

BEGIN
    v_tenant := p_params->>'tenant';
    v_searchindex := p_params->>'searchindex';
    v_name := p_params->>'name';
    v_description := p_params->>'description';
    v_desk_id := p_params->>'desk_id';
    v_user_id := p_params->>'user_id';
    v_fromdatetime := p_params->>'fromdatetime';
    v_todatetime := p_params->>'todatetime';
         

    INSERT INTO public.booking (
    id,
    created_at,
    updated_at,
        created_by, 
        updated_by, 
        tenant,
        searchindex,
        name,
        description,
        desk_id,
        user_id,
        fromdatetime,
        todatetime
    )
    VALUES (
        DEFAULT,
        DEFAULT,
        DEFAULT,
        p_actor_name, 
        p_actor_name,  -- Use the same value for updated_by
        v_tenant,
        v_searchindex,
        v_name,
        v_description,
        v_desk_id,
        v_user_id,
        v_fromdatetime,
        v_todatetime
    )
    RETURNING id INTO p_id;

       p_auditlog_params := jsonb_build_object(
        'tenant', '',
        'searchindex', '',
        'name', 'create_booking',
        'status', 'success',
        'description', '',
        'action', 'create_booking',
        'entity', 'booking',
        'entityid', -1,
        'actor', p_actor_name,
        'metadata', p_params
    );

    -- Call the create_auditlog procedure
    CALL proc.create_auditlog(p_actor_name, p_auditlog_params, v_audit_id);
END;
$BODY$
;
